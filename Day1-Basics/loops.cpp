// program to under stand loops
// program to print name n times

#include <iostream>
using namespace std;

int main() {
    // taking stopting limit from the user
    int input;
    cout << "Times to print Name: ";
    cin >> input;
    
    // usign loops to print "Name" n times
    for (int i=0; i<input; i++) {
        cout << "Name" << endl;
    }
    
}
